#OPERATORS
#Operators are used to perform mathematical operations on numbers
num1 = 20
num2 = 6
print(num1+num2) #Addition operator
print(num1-num2)  #Subtraction Operator
print(num1*num2)  #Multiplication operator
print(num1/num2)  #Division operator
print(num1%num2)  #Modulus operator or we can say it returns the remainder of the given division
print(num1//num2)  #Floor division or we can say it returns the integer part of the division and removes the decimal part if remains
print(num1**num2)  #Exponential operator or we can say it raises number to any power that is provided by using ** signs